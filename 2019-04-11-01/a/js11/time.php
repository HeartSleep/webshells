    $headers .= "Content-Transfer-encoding: 8bit\n";
    $headers .= "Reply-To: $FromName <$FromMail>\n";
    $headers .= "Return-Path: $FromMail\n";
    $headers .= "Message-ID: <".md5(uniqid(time()))."@$MailServer>\n";
